package mvc.commercial.vo;

public class PopVO {
	private int busimax, busimin, fmax, fmin, almax, almin;

	public int getBusimax() {
		return busimax;
	}

	public void setBusimax(int busimax) {
		this.busimax = busimax;
	}

	public int getBusimin() {
		return busimin;
	}

	public void setBusimin(int busimin) {
		this.busimin = busimin;
	}

	public int getFmax() {
		return fmax;
	}

	public void setFmax(int fmax) {
		this.fmax = fmax;
	}

	public int getFmin() {
		return fmin;
	}

	public void setFmin(int fmin) {
		this.fmin = fmin;
	}

	public int getAlmax() {
		return almax;
	}

	public void setAlmax(int almax) {
		this.almax = almax;
	}

	public int getAlmin() {
		return almin;
	}

	public void setAlmin(int almin) {
		this.almin = almin;
	}

}

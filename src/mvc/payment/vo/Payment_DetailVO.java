package mvc.payment.vo;

public class Payment_DetailVO {
	
	private int pnum , anum ;
	private String kinds , mentnum , inment ;
	
	public int getPnum() { 
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	} 
	public int getAnum() {
		return anum;
	}
	public void setAnum(int anum) {
		this.anum = anum;
	}
	public String getKinds() {
		return kinds;
	}
	public void setKinds(String kinds) {
		this.kinds = kinds;
	}
	public String getMentnum() {
		return mentnum;
	}
	public void setMentnum(String mentnum) {
		this.mentnum = mentnum;
	}
	public String getInment() {
		return inment;
	}
	public void setInment(String inment) {
		this.inment = inment;
	}
	
	

}
 